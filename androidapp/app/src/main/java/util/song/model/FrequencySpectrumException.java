public class FrequencySpectrumException extends Exception {
	public FrequencySpectrumException() {
		super();
	}

	public FrequencySpectrumException(String message) {
		super(message);
	}

	public FrequencySpectrumException(String message, Throwable cause) {
		super(message, cause);
	}

	public FrequencySpectrumException(Throwable cause) {
		super(cause);
	}
}
